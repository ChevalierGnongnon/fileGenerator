#!/bin/bash

mode=$(echo "$1" | tr a-z A-Z)
cols=$(tput cols 2>/dev/null || echo 80)

# ASCII art pour TXT
ascii_txt=(
	"_______________  ______________"
	"\__    ___/\   \/  /\__    ___/"
	"  |    |    \     /   |    |   "
	"  |    |    /     \   |    |   "
	"  |____|   /___/\  \  |____|   "
	"                 \_/           "
)

ascii_csv=(
	"_________   _____________   ____"
	"\_   ___ \ /   _____/\   \ /   /"
	"/    \  \/ \_____  \  \   Y   / "
	"\     \____/        \  \     /  "
	" \______  /_______  /   \___/   "
	"        \/        \/            "
)

ascii_cpp09_ex00=(
	"_____________________________  _______  ________  _______________  __________  _______   "
	"\_   ___ \______   \______   \ \   _  \/   __   \ \_   _____/\   \/  /\   _  \ \   _  \  "
	"/    \  \/|     ___/|     ___/ /  /_\  \____    /  |    __)_  \     / /  /_\  \/  /_\  \ "
	"\     \___|    |    |    |     \  \_/   \ /    /   |        \ /     \ \  \_/  /\  \_/  / "
	" \______  /____|    |____|      \_____  //____/   /_______  //___/\  \ \_____/  \_____/  "
	"        \/                            \/                  \/       \_/                   "
)

ascii_cub3d=(
	"_________  ____ _____________________ ________   "
	"\_   ___ \|    |   \______   \_____  \\______ \  "
	"/    \  \/|    |   /|    |  _/ _(__  < |    |  \ "
	"\     \___|    |  / |    |   \/       \|       \\"
	" \______  /______/  |______  /______  /_______  /"
	"        \/                 \/       \/        \/ "
)
ascii_gnl=(
	"  ________  _______  .____     "
	" /  _____/  \      \ |    |    "
	"/   \  ___  /   |   \|    |    "
	"\    \_\  \/    |    \    |___ "
	" \______  /\____|__  /_______\\"
	"        \/         \/        \/"
)

ascii_solong=(
	"  _________________    .____    ________    _______    ________ "
	" /   _____/\_____  \   |    |   \_____  \   \      \  /  _____/ "
	" \_____  \  /   |   \  |    |    /   |   \  /   |   \/   \  ___ "
	" /        \/    |    \ |    |___/    |    \/    |    \    \_\ \\"
	"/_______  /\_______  / |_______ \_______  /\____|__  /\______  /"
	"        \/         \/          \/       \/         \/        \/ "
)

# ASCII par défaut (si tu veux en ajouter d'autres plus tard)
ascii_default=(
	"=====default mode====="
)

# Choix selon mode
case "$mode" in
  TXT)         art=("${ascii_txt[@]}") ;;
  CSV)         art=("${ascii_csv[@]}") ;;
  CPP09_EX00)  art=("${ascii_cpp09_ex00[@]}") ;;
  CUB3D)       art=("${ascii_cub3d[@]}") ;;
  GNL)         art=("${ascii_gnl[@]}") ;;
  SOLONG)      art=("${ascii_solong[@]}") ;;
  *)           art=("${ascii_default[@]}") ;;
esac


# Affichage centré
echo ""
for line in "${art[@]}"; do
  padding=$(( (cols - ${#line}) / 2 ))
  printf "%*s%s\n" "$padding" "" "$line"
done
echo ""
